void	ft_putchar(char c);

void	ft_print_comb(void)
{
	int i;
	int d1;
	int d2;
	int d3;

	i = 0;
	while (i <= 789)
	{
		d1 = i / 100;
		d2 = (i / 10) % 10;
		d3 = i % 10;
		if ((d1 < d2) && (d2) < (d3))
		{
			ft_putchar('0' + d1);
			ft_putchar('0' + d2);
			ft_putchar('0' + d3);
			if (i < 789)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i++;
	}
}
