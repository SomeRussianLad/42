char	*ft_rot42(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		if ((str[i] >= 'a' || str[i] >= 'A') && (str[i] <= 'p' || str[i] <= 'P'))
			str[i] += 10;
		if ((str[i] >= 'q' || str[i] >= 'Q') && (str[i] <= 'z' || str[i] <= 'Z'))
			str[i] -= 16;
		i++;
	}
	return (str);
}
