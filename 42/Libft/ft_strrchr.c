char	*ft_strrchr(const char *s, int c)
{
	char	*c_s;
	char	*bgn;

	c_s = (char *)s;
	bgn = (char *)s;
	while (*c_s)
		c_s++;
	while (c_s > bgn)
	{
		if (*c_s == c)
			return (c_s);
		c_s--;
	}
	return (0);
}
