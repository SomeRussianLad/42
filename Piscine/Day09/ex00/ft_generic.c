#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	unsigned int i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}

void	ft_generic(void)
{
	ft_putstr("Tut tut ; Tut tut");
}

int		main(void)
{
	ft_generic();
	return (0);
}
