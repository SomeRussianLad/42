void	ft_putchar(void);

void	ft_print_comb2(void)
{
	int i;

	i = 1;
	while (i <= 9899)
	{
		if ((i % 1000 / 100) < (i % 10))
		{
			ft_putchar('0' + (i / 1000));
			ft_putchar('0' + ((i % 1000) / 100));
			ft_putchar(' ');
			ft_putchar('0' + ((i % 100) / 10));
			ft_putchar('0' + (i % 10));
			if (i != 9899)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i++;
	}
}
